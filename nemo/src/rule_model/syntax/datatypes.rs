//! This module defines constants relating to the data types recognized by nemo.

/// Can represent values of any type
pub(crate) const DATATYPE_ANY: &str = "any";
/// Represents string values
pub(crate) const DATATYPE_STRING: &str = "string";
/// Represents 64bit integer values
pub(crate) const DATATYPE_INT: &str = "int";
/// Represents 64bit floating-point values
pub(crate) const DATATYPE_DOUBLE: &str = "double";
/// Represents 32bit floating-point values
pub(crate) const DATATYPE_FLOAT: &str = "float";
